import { addIcon } from 'obsidian';

/**
 * Core-icon override module — makes Obsidian's own chrome (menus, ribbon,
 * mobile navbar, settings, quick-switcher) render Huge Icons instead of the
 * default Lucide set, so the whole app speaks one iconographic language.
 *
 * How it works
 * ------------
 * Obsidian resolves an icon name (e.g. `search`, `chevron-left`) through a
 * global registry. `addIcon(name, svg)` writes into that registry, and calling
 * it with a CORE Lucide name overrides the built-in glyph app-wide. This module
 * re-registers ~28 curated core names with Huge Icons "Stroke Rounded" glyphs.
 *
 * Constraints honoured
 * --------------------
 * - **Mobile-safe**: pure `addIcon` + string data, no Node/Electron builtins,
 *   so the override reaches iPhone via Sync for free (Portal is mobile-safe).
 * - **viewBox quirk**: `addIcon()` always forces `viewBox="0 0 100 100"`, so the
 *   24×24 Huge Icons grid is wrapped in `<g transform="scale(4.166667)">`
 *   (100 / 24) — the exact pattern already used by `hi-panel-left` in main.ts.
 * - **Irreversible at runtime**: there is no `removeIcon()`; once a core name is
 *   overridden it stays overridden for the session. Turning the setting OFF
 *   therefore only takes effect after an app restart (surfaced honestly in the
 *   setting description). `installCoreIcons()` is called only when the setting
 *   is ON, before any view renders.
 * - **Living curated list**: any core Lucide name NOT in the table below stays
 *   Lucide. This is a deliberate curation, never a blind bulk replace — add a
 *   row only when the Huge Icon reads correctly at chrome sizes.
 *
 * Sources (priority order)
 * ------------------------
 * 1. Existing `hi-*` glyphs already shipped in main.ts / nav/nav-block.ts
 *    (`file`, `calendar` reuse those exact paths).
 * 2. Vault icon pack `_system/icons/huge-icons/` (same normalized format).
 * 3. hugeicons.com free set (`@hugeicons/core-free-icons`, MIT) for the rest.
 *
 * Mapping table: lucide core name → Huge Icons glyph
 * --------------------------------------------------
 *   search           → Search01
 *   plus             → PlusSign
 *   menu             → Menu01
 *   chevron-left     → ArrowLeft01     (chevron style)
 *   chevron-right    → ArrowRight01    (chevron style)
 *   chevron-up       → ArrowUp01       (chevron style)
 *   chevron-down     → ArrowDown01     (chevron style)
 *   settings         → Settings01      (cog / gear)
 *   gear             → Settings01      (alias of settings)
 *   more-vertical    → MoreVertical
 *   more-horizontal  → MoreHorizontal
 *   x                → Cancel01
 *   calendar         → hi-calendar     (reused from nav-block.ts)
 *   folder           → Folder01
 *   file             → File01          (== existing hi-file-01)
 *   star             → Star
 *   trash            → Delete02
 *   copy             → Copy01
 *   link             → Link01
 *   tag              → Tag01
 *   pencil           → PencilEdit01
 *   arrow-left       → ArrowLeft02     (full arrow, not chevron)
 *   arrow-right      → ArrowRight02    (full arrow, not chevron)
 *   check            → Tick02
 *   info             → InformationCircle
 *   filter           → Filter
 *   hash             → Hash
 *   home             → Home09
 */

/** Common attributes for every Huge Icons "Stroke Rounded" glyph wrapper. */
const WRAP_OPEN =
  '<g transform="scale(4.166667)" fill="none" stroke="currentColor" ' +
  'stroke-linecap="round" stroke-linejoin="round" stroke-width="1.5">';
const WRAP_CLOSE = '</g>';

/**
 * Wrap one or more raw `d` path strings (on the 24×24 Huge Icons grid) into the
 * canonical `<g transform="scale(4.166667)">…</g>` body `addIcon()` expects.
 */
function svg(...paths: string[]): string {
  return WRAP_OPEN + paths.map((d) => `<path d="${d}"/>`).join('') + WRAP_CLOSE;
}

